        <h2>Grundlegender Ablauf vom Abruf der Seite bis zur Darstellung der Seite (am Beispiel der Startseite)</h2>
        <ol>
            <li>index.php ruft die Konfigurationsdateien ab</li>
            <li>index.php ruft den Autoloader ab und lädt somit alle PHP Klassen</li>
            <li>index.php initialisiert den Kernel
                <ol>
                    <li>Alle Services werden initialisiert</li>
                    <li>Datenbankverbindung wird vorbereitet</li>
                    <li>Session wird geprüft</li>
                    <li>Controller-Routen werden abgerufen</li>
                </ol>
            </li>
            <li>index.php führt Befehl handleRequest im RequestService aus
                <ol>
                    <li>Es wird ein Match anhand der URL gesucht mittels regulären Ausdrücken</li>
                    <li>Es gibt ein Match für '/' im IndexController</li>
                    <li>Es wird die Methode index() im IndexController ausgeführt</li>
                </ol>
            </li>
            <li>
                index() im IndexController übernimmt die Kontrolle
                <ol>
                    <li>Es werden Parameter, wie der Titel, die stylesheets gesetzt</li>
                    <li>Es werden alle sichtbaren Produkte mittels des ModelServices geladen</li>
                    <li>Es werden die Wunschlisten-Relationen des Nutzers geladen (wenn angemeldet)</li>
                    <li>Die Datei index.php im views Ordner wird als zu rendernde View festgelegt</li>
                </ol>
            </li>
            <li>
                handleRequest() im RequestService übernimmt wieder die Kontrolle
                <ol>
                    <li>Es wird die Methode renderTemplate in der Controller-Basisklasse abgerufen</li>
                    <li>Es wird das Template base im Ordner views/templates/ geladen</li>
                    <li>Die Parameter aus dem Controller werden übergeben an das Template</li>
                </ol>
            </li>
            <li>
                Das Template base.php wird geladen
                <ol>
                    <li>Das Template lädt das HTML-Gerüst fürs Frontend</li>
                    <li>Komponenten, wie die Navigation werden geladen</li>
                    <li>Die View wird gerendert über den Controller</li>
                </ol>
            </li>
            <li>
                Die Datenbankverbindung wird geschlossen und die Webseite ist fertig geladen
            </li>
        </ol>
        <h2>Besondere Funktionalitäten</h2>
        <h3>handleRequest und matchesRoute</h3>
        <p>Bereits zu Beginn des Projektes haben wir untereinander ausgemacht, ein eigenes System zu schreiben für die Webseite, mit eigenen Handerln, Controller uvm. Dies ermöglichte uns von komplett dynamischen URLs (bspw. index.php?c=shop&a=cart) auf semi-statisch, semi-dynamische URLs (bspw. /category/4) zu wechseln. Alternativ funktioniert das Projekt aber auch mit dynamischen URLs durch eine Änderung in der Konfiguration der index.php.</p>
        <p>Das arbeiten mit diesen URLs brach zu Beginn einige Probleme herbei, welche aber durch u.a. reguläre Ausdrücke bewältigt werden konnten. Innerhalb des RequestServices mit der Methode matchesRoute ist es nun möglich URL-Pattern auszulesen und nach Matches zu suchen, selbst mit dynamischen Werten, bspw /product/{id}.</p>
        <h4>Arbeitsweise:</h4>
        <ol>
            <li>Controller werden nacheinander durchlaufen und Routen werden erfasst</li>
            <li>Jede Route wird geprüft, ob sie übereinstimmt (mit und ohne dynmische Inhalte)</li>
            <li>Sobald ein Match gefunden wurde, wird die zugewiesene Methode mit oder ohne Parametern ausgeführt, andernfalls wird eine 404-Seite dargestellt</li>
        </ol>
